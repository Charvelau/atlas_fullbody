function [A, B] = getAB(x0, u, dt, m, g, z)
    F = u(3);
    z = x0(3);
    x = x0(1);
    y = x0(2);
    px = u(1);
    py = u(2);
    
    A = ...
[            1,            0,                       0, dt,  0,  0; ...
            0,            1,                       0,  0, dt,  0; ...
            0,            0,                       1,  0,  0, dt; ...
 (F*dt)/(m*z),            0, (F*dt*(px - x))/(m*z^2),  1,  0,  0; ...
            0, (F*dt)/(m*z), (F*dt*(py - y))/(m*z^2),  0,  1,  0; ...
            0,            0,                       0,  0,  0,  1];
 
 
B = ...
[             0,             0,                    0; ...
             0,             0,                    0; ...
             0,             0,                    0; ...
 -(F*dt)/(m*z),             0, -(dt*(px - x))/(m*z); ...
             0, -(F*dt)/(m*z), -(dt*(py - y))/(m*z); ...
             0,             0,                 dt/m];
end